#increasing triangle pattern 
'''
* (i=0)
* *(i=1)
* * * (i=2)
* * * * (i=3)
* * * * *(i=4)
'''



print("Enter a number:")
n = int(input())
for i in range(n):
    for j in range(i+1):
        print("*", end = " ")
    print()



