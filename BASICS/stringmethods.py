#capitalize - converts 1st char to UC
a="hello, I am Bhavana"
b=a.capitalize()
print(b)#Hello, i am bhavana

#casefold - converts string to LC

a="hello, I am Bhavana"
v=a.casefold()
print(v)#hello, i am bhavana

#centre - returns a centered string
f = "Bhavana"
d = f.center(20)
print(d)

#encode - Returns an encoded version of the string
x="fruit"
g=x.encode()
print(g)

#endswith

s='hi nice to meet you'
r=s.endswith("u")
print(r)

#isalnum

d='don2334fvc'
c=d.isalnum()
print(c)
