#upper 
a = "Hello, World!"
print(a.upper())

#lower 
a = "Hello, World!"
print(a.lower())

#strip  - removes whitespaces in the beginning or in the end 
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

#replace 

s="Bhavana"
print(s.replace("a","i"))

#split 

a = "Hello, my, name, is, World!"
print(a.split(","))

#string concatenation
a="Hey"
b="Hello"
print(a+ " "+b) # to return with space

